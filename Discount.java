package lesson3;
public class Discount {
    public static void main(String[] args) {
        // Create new variable product A and B
        int a = 1000;
        int b = 500;
        // Make sum
        int sum = a + b;
        // Make discount 10%
        int discount = (sum / 10) * 9;
        System.out.println(discount);
    }
}
